# ── Helpers ───────────────────────────────────────────────────────────────────

def _call_llm(model: str, prompt: str) -> str:
    """Call the LLM via Ollama REST API. Replace with your preferred adapter."""
    import urllib.request, json
    payload = json.dumps({"model": model, "prompt": prompt, "stream": False}).encode()
    req = urllib.request.Request(
        "http://localhost:11434/api/generate",
        data=payload,
        headers={"Content-Type": "application/json"},
    )
    with urllib.request.urlopen(req, timeout=120) as resp:
        return json.loads(resp.read())["response"].strip()


def _write(path: str, content: str) -> None:
    """Write content to a file, creating parent dirs as needed."""
    p = Path(path)
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(content, encoding="utf-8")

# ── Nodes  (each mirrors one GENERATE / EVALUATE block) ──────────────────────

class DraftNode(Node):
    # SPL: initial draft

    def prep(self, shared):
        # Extract — pull INPUT vars from shared store
        problem = shared["problem"]
        max_iterations = shared["max_iterations"]
        return problem, max_iterations

    def exec(self, prep_res):
        # Transform — LLM call
        problem, max_iterations = prep_res
        prompt = f"""Generate the next step in chain-of-thought reasoning for the following problem.

Problem context: {problem}

Previous thoughts:

Current step: 1

Current planning state (list of tasks with status):
[]

Respond in YAML format with:
thought: "your detailed reasoning for this step"
planning_update:
  - status: "completed|in_progress|pending"
    task: "description of planning item"
    step_number: number
    priority: "high|medium|low"
next_thought_needed: true|false
conclusion: "final answer if reasoning is complete, otherwise leave empty"
confidence: 0.0-1.0"""
        return _call_llm("gemma3", prompt)

    def post(self, shared, prep_res, exec_res):
        # Load — write results back to shared store
        shared["current"] = exec_res
        return "critique"

class CritiqueNode(Node):
    # SPL: GENERATE critique(@current) INTO @feedback
    # post() mirrors EVALUATE @feedback WHEN contains("None") + WHILE guard

    def prep(self, shared):
        # Extract — pull current draft, iteration counter, and model
        return shared["current"], shared.get("iteration", 0)

    def exec(self, prep_res):
        # Transform — LLM critique call
        current, iteration = prep_res
        # Parse the current response to check if next_thought_needed
        if "next_thought_needed: false" in current.lower():
            return "COMPLETE"
        elif iteration >= 2:  # Limit iterations
            return "MAX_ITERATIONS"
        else:
            return "CONTINUE"

    def post(self, shared, prep_res, exec_res):
        # Load — save feedback; route based on EVALUATE + WHILE guard
        shared["feedback"] = exec_res
        _, i = prep_res
        shared["iteration"] = i + 1

        if exec_res == "COMPLETE":
            print(f"Reasoning complete | iterations={i+1}")
            return "commit"
        elif exec_res == "MAX_ITERATIONS" or i >= shared.get("max_iterations", 3):
            print(f"Max iterations reached | iterations={i+1}")
            return "commit"
        else:
            print(f"Continuing reasoning | iteration={i+1}")
            return "refine"

class RefineNode(Node):
    # SPL: GENERATE refine(@current, @feedback) INTO @current

    def prep(self, shared):
        # Extract — pull current draft, feedback, iteration counter, and problem
        return shared["current"], shared["feedback"], shared.get("iteration", 0), shared["problem"]

    def exec(self, prep_res):
        # Transform — LLM refinement call (next step of reasoning)
        current, feedback, iteration, problem = prep_res

        prompt = f"""Generate the next step in chain-of-thought reasoning for the following problem.

Problem context: {problem}

Previous thoughts:
--- Step {iteration} ---
{current}

Current step: {iteration + 1}

Current planning state (list of tasks with status):
{feedback}

Respond in YAML format with:
thought: "your detailed reasoning for this step"
planning_update:
  - status: "completed|in_progress|pending"
    task: "description of planning item"
    step_number: number
    priority: "high|medium|low"
next_thought_needed: true|false
conclusion: "final answer if reasoning is complete, otherwise leave empty"
confidence: 0.0-1.0"""

        return _call_llm("gemma3", prompt)

    def post(self, shared, prep_res, exec_res):
        # Load — update draft and increment iteration counter
        shared["current"] = exec_res
        return "critique"

class CommitNode(Node):
    # SPL: RETURN @current WITH status, iterations

    def prep(self, shared):
        return shared.get("current", ""), shared.get("iteration", 0), shared.get("feedback", "")

    def exec(self, prep_res):
        return prep_res

    def post(self, shared, prep_res, exec_res):
        # Load — write final output and log status
        current, i, feedback = exec_res
        completed = "next_thought_needed: false" in current.lower()
        status = "complete" if completed else "max_iterations"
        print(f"\nStatus:     {status}")
        print(f"Iterations: {i}")
        print(f"Problem:    {shared.get('problem', '')}")
        print(f"\nFinal Response:\n{current}")
        shared["status"] = status
        return None

# ── Flow wiring  (mirrors WORKFLOW control flow) ─────────────────────────────
# SPL:  draft → WHILE loop → EVALUATE → commit or refine → critique
#
# PocketFlow conditional edges via post() return value (action string):
#   critique.post() → "commit"  (EVALUATE [APPROVED] or max_iterations)
#   critique.post() → "refine"  (else branch)
#   refine.post()   → "critique" (loop back)
#   draft.post()    → "critique" (initial transition)

def build_flow() -> Flow:
    draft   = DraftNode()
    critique = CritiqueNode()
    refine  = RefineNode()
    commit  = CommitNode()

    # SPL: initial draft → critique
    draft   - "critique" >> critique

    # SPL: EVALUATE + WHILE guard → commit or refine
    critique - "commit"  >> commit
    critique - "refine"  >> refine

    # SPL: WHILE back-edge — refine → critique
    refine  - "critique" >> critique

    return Flow(start=draft)

# ── Entry point  (SPL: built into CLI — `spl3 run ...`) ────────────────────────

@click.command()
@click.option("--problem", required=True)
@click.option("--max-iterations", default=3, show_default=True, type=int)
def main(problem: str, max_iterations: int):
    # SPL: INPUT @vars → shared store (ETL staging area)
    shared = {
        "problem": problem,
        "max_iterations": max_iterations,
        "iteration": 0,
        "current": "",
        "feedback": "",
    }
    build_flow().run(shared)


if __name__ == "__main__":
    main()